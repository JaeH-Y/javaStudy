package sec01.exam01;

public class IfExample {
	public static void main(String[] args) {
		int score = 93;
		
		if(score >= 90) {
			System.out.println("점수가 90보다 이상입니다.");
			System.out.println("등급은 A 입니다.");
		}

		// 중괄호 생략하고 테스트
		if(score < 90) 
			System.out.println("점수가 90보다 작습니다.");
			System.out.println("등급은 B 입니다.");
		
		
		
		
		
		
		
		
	}
}
